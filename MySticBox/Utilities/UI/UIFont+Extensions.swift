import UIKit

extension UIFont {
    static func msb_PTMonoBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "PTMono-Bold", size: size)!
    }
    
    static func msb_PTMonoRegular(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "PTMono-Regular", size: size)!
    }
}
